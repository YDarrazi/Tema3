public class EjemploMultiConFor {
    public static void main(String[]args){
        for (int k=100, j=1; k>j ; k++, j--){
            System.out.println( " " );
        }
        for(int k=100, j=1; k<=100&&j>0; k++,j--){
                System.out.println( k+" "+j );
        }
    }
    
}
